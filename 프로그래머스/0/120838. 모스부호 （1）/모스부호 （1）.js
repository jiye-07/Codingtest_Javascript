function solution(letter) {
  const morse = { 
    ".-":"a","-...":"b","-.-.":"c","-..":"d",".":"e",
    "..-.":"f","--.":"g","....":"h","..":"i",".---":"j",
    "-.-":"k",".-..":"l","--":"m","-.":"n","---":"o",
    ".--.":"p","--.-":"q",".-.":"r","...":"s","-":"t",
    "..-":"u","...-":"v",".--":"w","-..-":"x","-.--":"y","--..":"z"
  };

  return letter
    .split(" ")     // 공백 기준으로 모스부호를 배열로 분리
    .map(code => morse[code])  // 각각을 알파벳으로 변환
    .join("");      // 배열을 문자열로 합침
}